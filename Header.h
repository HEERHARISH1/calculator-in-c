int sub(int a, int b)
{
	int sub;
	sub = a - b;
	return sub;
}

int add(int a, int b)
{
	int add;
	add = a + b;
	return add;
}
int mult(int a, int b)
{
	int mult;
	mult = a * b;
	return mult;
}
float division(float a, float b)
{
	float div;
	div = a / b;
	return div;
}